#include "3-calc.h"

/**
*op_add -  calcilate the sum
*Return: the sum of a and b
*@a: input as an int
*@b: input as an int
*/


int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub -  calcilate the difference
*Return:  the difference of a and b
*@a: input as an int
*@b: input as an int
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
*op_mul -  calcilate the multiplication
*Return:  the multiplication of a and b
*@a: input as an int
*@b: input as an int
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
*op_div -  calculate the division
*Return:  the division of a and b
*@a: input as an int
*@b: input as an int
*/

int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod -  calculate the modulo
*Return:  the modulo of a and b
*@a: input as an int
*@b: input as an int
*/
int op_mod(int a, int b)
{
	return (a % b);
}
